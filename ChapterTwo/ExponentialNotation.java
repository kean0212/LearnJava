import java.util.*;

public class ExponentialNotation {
    public static void main(String[] args) {
        float minFloat = -3.4E38f;
        float maxFloat = 3.4E38f;
        System.out.println("min float: " + minFloat + " max float: " + maxFloat);

        double minDouble = -1.79E308;
        double maxDouble = 1.79E308;
        System.out.println("min double : " + minDouble + " max double: " + maxDouble);
    }
}